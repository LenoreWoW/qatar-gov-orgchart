import * as React from "react";
import { cn } from "@/lib/utils";
import { motion } from "framer-motion";

// Qatar-themed Card with Aceternity-style animations
const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { animate?: boolean }
>(({ className, animate = false, children, ...props }, ref) => {
  const content = (
    <div
      ref={ref}
      className={cn(
        "rounded-xl border bg-white text-card-foreground shadow-sm transition-all duration-200",
        "border-qatar-maroon/10 hover:border-qatar-maroon/20",
        "hover:shadow-lg",
        className
      )}
      {...props}
    >
      {children}
    </div>
  );

  if (animate) {
    return (
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.3 }}
        whileHover={{ scale: 1.02 }}
        whileTap={{ scale: 0.98 }}
      >
        {content}
      </motion.div>
    );
  }

  return content;
});
Card.displayName = "Card";

